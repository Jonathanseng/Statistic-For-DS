import statistics

# Create a list of numbers
numbers = [1, 2, 3, 4, 5]

# Find the mode of the list
mode = statistics.mode(numbers)

# Print the mode
print(mode)

# This code will print the following output:
3

# The mode of the list is 3 because it appears the most times in the list.

